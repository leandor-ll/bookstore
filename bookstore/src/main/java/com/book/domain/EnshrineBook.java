package com.book.domain;

public class EnshrineBook {
    private String eid;

    private String euname;

    private String ebid;

    private Integer eprice;

    private String epic;

    private String ebname;

    private String ecategory;

    public String getEid() {
        return eid;
    }

    public void setEid(String eid) {
        this.eid = eid;
    }

    public String getEuname() {
        return euname;
    }

    public void setEuname(String euname) {
        this.euname = euname;
    }

    public String getEbid() {
        return ebid;
    }

    public void setEbid(String ebid) {
        this.ebid = ebid;
    }

    public Integer getEprice() {
        return eprice;
    }

    public void setEprice(Integer eprice) {
        this.eprice = eprice;
    }

    public String getEpic() {
        return epic;
    }

    public void setEpic(String epic) {
        this.epic = epic;
    }

    public String getEbname() {
        return ebname;
    }

    public void setEbname(String ebname) {
        this.ebname = ebname;
    }

    public String getEcategory() {
        return ecategory;
    }

    public void setEcategory(String ecategory) {
        this.ecategory = ecategory;
    }
}